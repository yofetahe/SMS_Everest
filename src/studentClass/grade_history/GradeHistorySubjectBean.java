package studentClass.grade_history;

public class GradeHistorySubjectBean {
	
	private String sub_id;
	private String sub_name;
	private String sub_status;
	private String total_mark;
	private String total_pass_mark;
	
	public String getSub_id() {
		return sub_id;
	}
	public void setSub_id(String sub_id) {
		this.sub_id = sub_id;
	}
	public String getSub_name() {
		return sub_name;
	}
	public void setSub_name(String sub_name) {
		this.sub_name = sub_name;
	}
	public String getSub_status() {
		return sub_status;
	}
	public void setSub_status(String sub_status) {
		this.sub_status = sub_status;
	}
	public String getTotal_mark() {
		return total_mark;
	}
	public void setTotal_mark(String total_mark) {
		this.total_mark = total_mark;
	}
	public String getTotal_pass_mark() {
		return total_pass_mark;
	}
	public void setTotal_pass_mark(String total_pass_mark) {
		this.total_pass_mark = total_pass_mark;
	}
	

}
