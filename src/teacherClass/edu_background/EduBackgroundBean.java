package teacherClass.edu_background;

public class EduBackgroundBean {
	
	private String teb_id;
	private String field_of_study;
	private String name_of_institiute;
	private String graduation_date;
	private String award;
	private String ti_id;
	private String teb_status;
	
	
	public String getTeb_id() {
		return teb_id;
	}
	public void setTeb_id(String teb_id) {
		this.teb_id = teb_id;
	}
	public String getField_of_study() {
		return field_of_study;
	}
	public void setField_of_study(String field_of_study) {
		this.field_of_study = field_of_study;
	}
	public String getName_of_institiute() {
		return name_of_institiute;
	}
	public void setName_of_institiute(String name_of_institiute) {
		this.name_of_institiute = name_of_institiute;
	}
	public String getGraduation_date() {
		return graduation_date;
	}
	public void setGraduation_date(String graduation_date) {
		this.graduation_date = graduation_date;
	}
	public String getAward() {
		return award;
	}
	public void setAward(String award) {
		this.award = award;
	}
	public String getTi_id() {
		return ti_id;
	}
	public void setTi_id(String ti_id) {
		this.ti_id = ti_id;
	}
	public String getTeb_status() {
		return teb_status;
	}
	public void setTeb_status(String teb_status) {
		this.teb_status = teb_status;
	}
}
