package teacherClass.work_experience;

public class WorkExperienceBean {
	
	private String twe_id;
	private String name_of_institute;
	private String job_title;
	private String date_from;
	private String date_to;
	private String twe_status;
	private String ti_id;
	
	
	public String getTwe_id() {
		return twe_id;
	}
	public void setTwe_id(String twe_id) {
		this.twe_id = twe_id;
	}
	public String getName_of_institute() {
		return name_of_institute;
	}
	public void setName_of_institute(String name_of_institute) {
		this.name_of_institute = name_of_institute;
	}
	public String getJob_title() {
		return job_title;
	}
	public void setJob_title(String job_title) {
		this.job_title = job_title;
	}
	public String getDate_from() {
		return date_from;
	}
	public void setDate_from(String date_from) {
		this.date_from = date_from;
	}
	public String getDate_to() {
		return date_to;
	}
	public void setDate_to(String date_to) {
		this.date_to = date_to;
	}
	public String getTwe_status() {
		return twe_status;
	}
	public void setTwe_status(String twe_status) {
		this.twe_status = twe_status;
	}
	public String getTi_id() {
		return ti_id;
	}
	public void setTi_id(String ti_id) {
		this.ti_id = ti_id;
	}

}
